package vg.edu.pe.model;

public class TecnologiaM {

    private String CODTEC;
    private String DESCTEC;
    private String TIPOTEC;
    private String SUBTIPOTEC;

    public String getCODTEC() {
        return CODTEC;
    }

    public void setCODTEC(String CODTEC) {
        this.CODTEC = CODTEC;
    }

    public String getDESCTEC() {
        return DESCTEC;
    }

    public void setDESCTEC(String DESCTEC) {
        this.DESCTEC = DESCTEC;
    }

    public String getTIPOTEC() {
        return TIPOTEC;
    }

    public void setTIPOTEC(String TIPOTEC) {
        this.TIPOTEC = TIPOTEC;
    }

    public String getSUBTIPOTEC() {
        return SUBTIPOTEC;
    }

    public void setSUBTIPOTEC(String SUBTIPOTEC) {
        this.SUBTIPOTEC = SUBTIPOTEC;
    }
    
}
